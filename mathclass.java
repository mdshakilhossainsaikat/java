package com.valluk.again;
public class mathclass {
    public static void main(String[] args) {
        System.out.println("Absulute value of -9 is: "+Math.abs(-9.0));
        System.out.println("Absulute square root value of -9: "+Math.sqrt(Math.abs(-9.0)));
        System.out.println("Power value of absulute  square root of -9 is: "+Math.pow(Math.sqrt(Math.abs(-9.0)), 3));
        System.out.println("Value of PI: "+Math.PI);
        System.out.println("Value of TAU: "+Math.TAU);
        System.out.println(Math.ceil(Math.PI));
        System.out.println(Math.floor(Math.TAU));
    }
}
